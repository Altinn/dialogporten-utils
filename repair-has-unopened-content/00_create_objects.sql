\set ON_ERROR_STOP on

CREATE SCHEMA IF NOT EXISTS maintenance;

CREATE UNLOGGED TABLE IF NOT EXISTS maintenance.has_unopened_content_repair_state
(
    repair_name text PRIMARY KEY,
    window_start timestamp with time zone NOT NULL,
    window_end timestamp with time zone NOT NULL,
    discovery_cursor_created_at timestamp with time zone NOT NULL,
    discovery_cursor_id uuid NOT NULL,
    discovery_complete boolean NOT NULL DEFAULT false,
    discovery_batch_size integer NOT NULL DEFAULT 100000,
    process_batch_size integer NOT NULL DEFAULT 1000,
    created_at timestamp with time zone NOT NULL DEFAULT clock_timestamp(),
    updated_at timestamp with time zone NOT NULL DEFAULT clock_timestamp(),
    CONSTRAINT has_unopened_content_repair_window_valid CHECK (window_start < window_end),
    CONSTRAINT has_unopened_content_repair_discovery_batch_positive CHECK (discovery_batch_size > 0),
    CONSTRAINT has_unopened_content_repair_process_batch_positive CHECK (process_batch_size > 0)
);

CREATE UNLOGGED TABLE IF NOT EXISTS maintenance.has_unopened_content_repair_candidate
(
    repair_name text NOT NULL,
    dialog_id uuid NOT NULL,
    first_activity_created_at timestamp with time zone NOT NULL,
    first_activity_id uuid NOT NULL,
    source_activity_count bigint NOT NULL DEFAULT 0,
    discovered_at timestamp with time zone NOT NULL DEFAULT clock_timestamp(),
    processed_at timestamp with time zone,
    updated_at timestamp with time zone,
    skipped_reason text,
    PRIMARY KEY (repair_name, dialog_id)
);

CREATE INDEX IF NOT EXISTS ix_huc_repair_candidate_unprocessed
    ON maintenance.has_unopened_content_repair_candidate
        (repair_name, first_activity_created_at, first_activity_id, dialog_id)
    WHERE processed_at IS NULL;

CREATE UNLOGGED TABLE IF NOT EXISTS maintenance.has_unopened_content_repair_batch_log
(
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    repair_name text NOT NULL,
    phase text NOT NULL,
    batch_started_at timestamp with time zone NOT NULL,
    batch_finished_at timestamp with time zone NOT NULL,
    batch_seconds numeric NOT NULL,
    scanned_activities integer,
    candidates_touched integer,
    rows_selected integer,
    rows_updated integer,
    remaining_candidates bigint,
    cursor_created_at timestamp with time zone,
    cursor_id uuid,
    note text
);

