CREATE OR REPLACE FUNCTION maintenance.has_unopened_content_repair_has_unopened_content(p_dialog_id uuid)
RETURNS boolean
LANGUAGE sql
STABLE
AS $$
    SELECT COALESCE((
        SELECT
            EXISTS
            (
                SELECT 1
                FROM "DialogTransmission" t
                WHERE t."DialogId" = d."Id"
                  AND t."TypeId" NOT IN (7, 8) -- Submission, Correction
                  AND NOT EXISTS
                  (
                      SELECT 1
                      FROM "DialogActivity" a
                      WHERE a."TransmissionId" = t."Id"
                        AND a."TypeId" = 4 -- TransmissionOpened
                  )
            )
            OR
            (
                d."ServiceResourceType" = 'correspondenceservice'
                AND NOT EXISTS
                (
                    SELECT 1
                    FROM "DialogActivity" a
                    WHERE a."DialogId" = d."Id"
                      AND a."TypeId" = 16 -- CorrespondenceOpened
                )
            )
        FROM "Dialog" d
        WHERE d."Id" = p_dialog_id
    ), false);
$$;

CREATE OR REPLACE PROCEDURE maintenance.has_unopened_content_repair_discover(
    p_repair_name text DEFAULT 'has_unopened_content_repair_2026_02_25_2026_03_20',
    p_batch_size integer DEFAULT NULL
)
LANGUAGE plpgsql
AS $$
DECLARE
    v_started_at timestamp with time zone := clock_timestamp();
    v_finished_at timestamp with time zone;
    v_seconds numeric;
    v_state maintenance.has_unopened_content_repair_state%ROWTYPE;
    v_batch_size integer;
    v_scanned integer := 0;
    v_candidates_touched integer := 0;
    v_last_created_at timestamp with time zone;
    v_last_id uuid;
BEGIN
    SET LOCAL lock_timeout = '1s';
    SET LOCAL statement_timeout = '5min';

    SELECT *
    INTO v_state
    FROM maintenance.has_unopened_content_repair_state
    WHERE repair_name = p_repair_name
    FOR UPDATE;

    IF NOT FOUND THEN
        RAISE EXCEPTION 'Repair state % was not found. Run 00_create_objects.sql first.', p_repair_name;
    END IF;

    IF v_state.discovery_complete THEN
        RAISE NOTICE 'discover: repair=% discovery already complete at cursor=(%, %)',
            p_repair_name, v_state.discovery_cursor_created_at, v_state.discovery_cursor_id;
        RETURN;
    END IF;

    v_batch_size := COALESCE(p_batch_size, v_state.discovery_batch_size);

    CREATE TEMP TABLE IF NOT EXISTS huc_repair_activity_batch
    (
        created_at timestamp with time zone NOT NULL,
        activity_id uuid NOT NULL,
        dialog_id uuid NOT NULL
    ) ON COMMIT DROP;

    TRUNCATE huc_repair_activity_batch;

    INSERT INTO huc_repair_activity_batch (created_at, activity_id, dialog_id)
    SELECT a."CreatedAt", a."Id", a."DialogId"
    FROM "DialogActivity" a
    JOIN "Dialog" d ON d."Id" = a."DialogId"
    WHERE a."TypeId" IN (4, 16)
      AND a."CreatedAt" >= v_state.window_start
      AND a."CreatedAt" < v_state.window_end
      AND (a."CreatedAt", a."Id") > (v_state.discovery_cursor_created_at, v_state.discovery_cursor_id)
      AND d."HasUnopenedContent" = true
    ORDER BY a."CreatedAt", a."Id"
    LIMIT v_batch_size;

    GET DIAGNOSTICS v_scanned = ROW_COUNT;

    IF v_scanned = 0 THEN
        UPDATE maintenance.has_unopened_content_repair_state
        SET discovery_complete = true,
            updated_at = clock_timestamp()
        WHERE repair_name = p_repair_name;

        v_finished_at := clock_timestamp();
        v_seconds := EXTRACT(epoch FROM v_finished_at - v_started_at);

        INSERT INTO maintenance.has_unopened_content_repair_batch_log
        (
            repair_name,
            phase,
            batch_started_at,
            batch_finished_at,
            batch_seconds,
            scanned_activities,
            candidates_touched,
            cursor_created_at,
            cursor_id,
            note
        )
        VALUES
        (
            p_repair_name,
            'discover',
            v_started_at,
            v_finished_at,
            v_seconds,
            0,
            0,
            v_state.discovery_cursor_created_at,
            v_state.discovery_cursor_id,
            'discovery complete'
        );

        RAISE NOTICE 'discover: repair=% discovery complete, elapsed=%s',
            p_repair_name, ROUND(v_seconds, 3);
        RETURN;
    END IF;

    SELECT created_at, activity_id
    INTO v_last_created_at, v_last_id
    FROM huc_repair_activity_batch
    ORDER BY created_at DESC, activity_id DESC
    LIMIT 1;

    WITH first_activity AS
    (
        SELECT DISTINCT ON (dialog_id)
            dialog_id,
            created_at,
            activity_id
        FROM huc_repair_activity_batch
        ORDER BY dialog_id, created_at, activity_id
    ),
    activity_count AS
    (
        SELECT dialog_id, COUNT(*) AS source_activity_count
        FROM huc_repair_activity_batch
        GROUP BY dialog_id
    )
    INSERT INTO maintenance.has_unopened_content_repair_candidate
    (
        repair_name,
        dialog_id,
        first_activity_created_at,
        first_activity_id,
        source_activity_count
    )
    SELECT
        p_repair_name,
        fa.dialog_id,
        fa.created_at,
        fa.activity_id,
        ac.source_activity_count
    FROM first_activity fa
    JOIN activity_count ac ON ac.dialog_id = fa.dialog_id
    ON CONFLICT (repair_name, dialog_id) DO UPDATE
    SET first_activity_created_at = LEAST(
            maintenance.has_unopened_content_repair_candidate.first_activity_created_at,
            EXCLUDED.first_activity_created_at),
        first_activity_id = CASE
            WHEN EXCLUDED.first_activity_created_at < maintenance.has_unopened_content_repair_candidate.first_activity_created_at
                THEN EXCLUDED.first_activity_id
            ELSE maintenance.has_unopened_content_repair_candidate.first_activity_id
        END,
        source_activity_count = maintenance.has_unopened_content_repair_candidate.source_activity_count
            + EXCLUDED.source_activity_count;

    GET DIAGNOSTICS v_candidates_touched = ROW_COUNT;

    UPDATE maintenance.has_unopened_content_repair_state
    SET discovery_cursor_created_at = v_last_created_at,
        discovery_cursor_id = v_last_id,
        updated_at = clock_timestamp()
    WHERE repair_name = p_repair_name;

    v_finished_at := clock_timestamp();
    v_seconds := GREATEST(EXTRACT(epoch FROM v_finished_at - v_started_at), 0.001);

    INSERT INTO maintenance.has_unopened_content_repair_batch_log
    (
        repair_name,
        phase,
        batch_started_at,
        batch_finished_at,
        batch_seconds,
        scanned_activities,
        candidates_touched,
        cursor_created_at,
        cursor_id
    )
    VALUES
    (
        p_repair_name,
        'discover',
        v_started_at,
        v_finished_at,
        v_seconds,
        v_scanned,
        v_candidates_touched,
        v_last_created_at,
        v_last_id
    );

    RAISE NOTICE 'discover: repair=% scanned=% candidates_touched=% cursor=(%, %) elapsed=%s rate=% activities/s',
        p_repair_name,
        v_scanned,
        v_candidates_touched,
        v_last_created_at,
        v_last_id,
        ROUND(v_seconds, 3),
        ROUND(v_scanned / v_seconds, 1);
END;
$$;

CREATE OR REPLACE PROCEDURE maintenance.has_unopened_content_repair_process(
    p_repair_name text DEFAULT 'has_unopened_content_repair_2026_02_25_2026_03_20',
    p_batch_size integer DEFAULT NULL
)
LANGUAGE plpgsql
AS $$
DECLARE
    v_started_at timestamp with time zone := clock_timestamp();
    v_finished_at timestamp with time zone;
    v_seconds numeric;
    v_state maintenance.has_unopened_content_repair_state%ROWTYPE;
    v_batch_size integer;
    v_selected integer := 0;
    v_updated integer := 0;
    v_remaining bigint := 0;
BEGIN
    SET LOCAL lock_timeout = '1s';
    SET LOCAL statement_timeout = '5min';

    SELECT *
    INTO v_state
    FROM maintenance.has_unopened_content_repair_state
    WHERE repair_name = p_repair_name;

    IF NOT FOUND THEN
        RAISE EXCEPTION 'Repair state % was not found. Run 00_create_objects.sql first.', p_repair_name;
    END IF;

    v_batch_size := COALESCE(p_batch_size, v_state.process_batch_size);

    CREATE TEMP TABLE IF NOT EXISTS huc_repair_dialog_batch
    (
        dialog_id uuid PRIMARY KEY
    ) ON COMMIT DROP;

    CREATE TEMP TABLE IF NOT EXISTS huc_repair_updated_dialog
    (
        dialog_id uuid PRIMARY KEY
    ) ON COMMIT DROP;

    TRUNCATE huc_repair_dialog_batch;
    TRUNCATE huc_repair_updated_dialog;

    INSERT INTO huc_repair_dialog_batch (dialog_id)
    SELECT c.dialog_id
    FROM maintenance.has_unopened_content_repair_candidate c
    WHERE c.repair_name = p_repair_name
      AND c.processed_at IS NULL
    ORDER BY c.first_activity_created_at, c.first_activity_id, c.dialog_id
    LIMIT v_batch_size
    FOR UPDATE SKIP LOCKED;

    GET DIAGNOSTICS v_selected = ROW_COUNT;

    IF v_selected = 0 THEN
        SELECT COUNT(*)
        INTO v_remaining
        FROM maintenance.has_unopened_content_repair_candidate
        WHERE repair_name = p_repair_name
          AND processed_at IS NULL;

        v_finished_at := clock_timestamp();
        v_seconds := EXTRACT(epoch FROM v_finished_at - v_started_at);

        INSERT INTO maintenance.has_unopened_content_repair_batch_log
        (
            repair_name,
            phase,
            batch_started_at,
            batch_finished_at,
            batch_seconds,
            rows_selected,
            rows_updated,
            remaining_candidates,
            note
        )
        VALUES
        (
            p_repair_name,
            'process',
            v_started_at,
            v_finished_at,
            v_seconds,
            0,
            0,
            v_remaining,
            'no unprocessed candidates selected'
        );

        RAISE NOTICE 'process: repair=% selected=0 remaining=% elapsed=%s',
            p_repair_name, v_remaining, ROUND(v_seconds, 3);
        RETURN;
    END IF;

    WITH updated AS
    (
        UPDATE "Dialog" d
        SET "HasUnopenedContent" = false
        FROM huc_repair_dialog_batch b
        WHERE d."Id" = b.dialog_id
          AND d."HasUnopenedContent" = true
          AND NOT maintenance.has_unopened_content_repair_has_unopened_content(d."Id")
        RETURNING d."Id"
    )
    INSERT INTO huc_repair_updated_dialog (dialog_id)
    SELECT "Id"
    FROM updated;

    GET DIAGNOSTICS v_updated = ROW_COUNT;

    UPDATE maintenance.has_unopened_content_repair_candidate c
    SET processed_at = clock_timestamp(),
        updated_at = CASE WHEN u.dialog_id IS NOT NULL THEN clock_timestamp() ELSE c.updated_at END,
        skipped_reason = CASE
            WHEN u.dialog_id IS NOT NULL THEN NULL
            WHEN NOT EXISTS (SELECT 1 FROM "Dialog" d WHERE d."Id" = c.dialog_id) THEN 'dialog_not_found'
            WHEN EXISTS (SELECT 1 FROM "Dialog" d WHERE d."Id" = c.dialog_id AND d."HasUnopenedContent" = false) THEN 'already_false'
            WHEN maintenance.has_unopened_content_repair_has_unopened_content(c.dialog_id) THEN 'still_has_unopened_content'
            ELSE 'not_updated_unknown'
        END
    FROM huc_repair_dialog_batch b
    LEFT JOIN huc_repair_updated_dialog u ON u.dialog_id = b.dialog_id
    WHERE c.repair_name = p_repair_name
      AND c.dialog_id = b.dialog_id;

    SELECT COUNT(*)
    INTO v_remaining
    FROM maintenance.has_unopened_content_repair_candidate
    WHERE repair_name = p_repair_name
      AND processed_at IS NULL;

    v_finished_at := clock_timestamp();
    v_seconds := GREATEST(EXTRACT(epoch FROM v_finished_at - v_started_at), 0.001);

    INSERT INTO maintenance.has_unopened_content_repair_batch_log
    (
        repair_name,
        phase,
        batch_started_at,
        batch_finished_at,
        batch_seconds,
        rows_selected,
        rows_updated,
        remaining_candidates
    )
    VALUES
    (
        p_repair_name,
        'process',
        v_started_at,
        v_finished_at,
        v_seconds,
        v_selected,
        v_updated,
        v_remaining
    );

    RAISE NOTICE 'process: repair=% selected=% updated=% skipped=% remaining=% elapsed=%s rate=% dialogs/s',
        p_repair_name,
        v_selected,
        v_updated,
        v_selected - v_updated,
        v_remaining,
        ROUND(v_seconds, 3),
        ROUND(v_selected / v_seconds, 1);
END;
$$;

CREATE OR REPLACE VIEW maintenance.has_unopened_content_repair_summary AS
SELECT
    s.repair_name,
    s.window_start,
    s.window_end,
    s.discovery_cursor_created_at,
    s.discovery_cursor_id,
    s.discovery_complete,
    COUNT(c.dialog_id) AS candidates_total,
    COUNT(c.dialog_id) FILTER (WHERE c.processed_at IS NULL) AS candidates_unprocessed,
    COUNT(c.dialog_id) FILTER (WHERE c.processed_at IS NOT NULL) AS candidates_processed,
    COUNT(c.dialog_id) FILTER (WHERE c.updated_at IS NOT NULL) AS candidates_updated,
    COUNT(c.dialog_id) FILTER (WHERE c.skipped_reason = 'already_false') AS candidates_already_false,
    COUNT(c.dialog_id) FILTER (WHERE c.skipped_reason = 'still_has_unopened_content') AS candidates_still_has_unopened_content,
    COUNT(c.dialog_id) FILTER (WHERE c.skipped_reason = 'dialog_not_found') AS candidates_dialog_not_found,
    MAX(c.discovered_at) AS last_candidate_discovered_at,
    MAX(c.processed_at) AS last_candidate_processed_at
FROM maintenance.has_unopened_content_repair_state s
LEFT JOIN maintenance.has_unopened_content_repair_candidate c ON c.repair_name = s.repair_name
GROUP BY
    s.repair_name,
    s.window_start,
    s.window_end,
    s.discovery_cursor_created_at,
    s.discovery_cursor_id,
    s.discovery_complete;

DO $$
BEGIN
    RAISE NOTICE 'Created maintenance objects for has_unopened_content repair.';
END $$;
